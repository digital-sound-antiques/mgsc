#ifndef _XTYPES_H_
#define _XTYPES_H_
#include <stdint.h>
/** 8bit符号なしの整数型 */
typedef uint8_t UINT8;
/** 16bit符号なしの整数型 */
typedef uint16_t UINT16;
/** 32bit符号なしの整数型 */
typedef uint32_t UINT32;
/** 8bit符号付き整数型 */
typedef int8_t INT8;
/** 16bit符号付き整数型 */
typedef int16_t INT16;
/** 32bit符号付き整数型 */
typedef int32_t INT32;
#endif